<h1>¡ Consejos para embalaje ! </h1>
<ul type="square" class="Estilo4">
  <li>Escoja el       tamaño del embalaje en función de su contenido. Las cajas semivacías es       probable que se desmoronen y las demasiado llenas probablemente exploten. </li>
  <li>Utilice       siempre materiales de alta calidad en sus envíos. Tenga en cuenta la       fortaleza, el almohadillado y la durabilidad cuando seleccione los       envoltorios. </li>
  <li>Elija cajas de       cartón corrugado, con forros externos de gran calidad. Utilice cartón de       doble capa, idóneo para pesos grandes, en el envío de objetos de valor. </li>
  <li>Utilice       materiales de almohadillado, sobre todo para evitar que el contenido se       mueva. </li>
  <li>Utilice       correas, siempre que sea conveniente, como un modo seguro para sellar y       asegurar su caja. Utilice cinta adhesiva fuerte en aquellos casos en que       no pueda disponer de correas. </li>
  <li>Coloque los       bienes frágiles en el centro del embalaje, garantizando que no toquen las       esquinas. El objeto deberá estar almohadillado por todos lados. </li>
  <li>Asegúrese de       que los líquidos sean embalados en contenedores anti goteo, acompañados de       un material interno ligero y fuerte (por ejemplo, espuma de polietileno) y       sellados con una bolsa de plástico. Recuerde siempre que un embalaje       defectuoso puede dañar otros objetos de alrededor. </li>
  <li>Selle las       sustancias semilíquidas, grasientas o de olores fuertes con cinta adhesiva       y posteriormente envuélvalas en papel resistente a la grasa. Recuerde       siempre que un embalaje defectuoso puede dañar otros objetos de alrededor. </li>
  <li>Coloque polvos       y granos finos en bolsas de plástico fuertes, selladas de forma segura y       posteriormente embaladas en una caja de cartón madera. </li>
  <li>Utilice       etiquetas de “flecha hacia arriba” para materiales no sólidos. </li>
  <li>Embale de       nuevo sus regalos de forma adecuada. Podría ocurrir que muchos bienes       vendidos en envoltorios atractivos pudieran no ser adecuados para el       transporte. </li>
  <li>Utilice tubos       triangulares, y no cilindros tipo tubo, para embalar rollos de planos,       mapas o proyectos. </li>
  <li>Recuerde       siempre envolver los objetos pequeños en volantes adecuados. </li>
  <li>Rellene la dirección       con claridad y completamente, utilice letras mayúsculas cuando la escriba       a mano, para facilitar su lectura al personal de despacho. </li>
  <li>Cuando       transporte objetos punzantes, como cuchillos o tijeras, asegúrese de que       protege convenientemente las puntas y filos. Para ello puede utilizar       cartón duro. Fije el material de protección con total seguridad, para       evitar que pueda caerse accidentalmente durante el tránsito. </li>
  <li>Utilice       divisores de cartón cuando envíe material plano y frágil (como, por       ejemplo, discos de vinilo, vasos, platos, etc.). </li>
  <li>Si desea enviar  vidrios o parabrisas, debe de hacerlo dentro de una baja de madera adecuada según la dimensión que tengan.</li>
  </ul>
